package ayhay.dataStructures;

public class StringScore {

	private String s;
	private double score;
	
	public StringScore(String s, double score){
		this.s = s;
		this.score = score;
	}
	
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	public double getScore() {
		return score;
	}
	public void setScore(double score) {
		this.score = score;
	}
	
	
}
